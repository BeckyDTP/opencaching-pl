<h1>Jakie dane gromadzimy</h1>
<p>Niektóre usługi Opencaching wymagają rejestracji użytkownika. Rejestracja składa się z dwóch kroków.
    Po podaniu ważnego adresu e-mail zostaje przesłany kod aktywacyjny.
    Po zalogowaniu można dokonywać zmiany danych w swoim profilu.

    Do rejestracji potrzebne są:
<ul>
    <li>Ważny adres e-mail
        Dobrowolnie/opcjonalnie można podać:
    <li>Kraj</ul>

<p>Nie przekazujemy osobom/podmiotom trzecim żadnych danych bez Twojego pozwolenia.
<p>Nazwa użytkownika, koordynaty, kraj tak jak inne dane mogą być przeglądane przez
    innych użytkowników Opencaching.pl, a także przez inne strony internetowe mogą być pobrane do celów statystycznych.
    Zastrzegamy sobie prawo odmówienia dostępu do danych osobowych.
    Jeśli nie chcesz, aby Twoje dane były przeglądane/przekazywane możesz nie podawać żadnych danych lub podać
    nieprawdziwe.
<p>Skrzynki, logi i obrazki nie należą do danych osobistych i będą przez nas przekazywane osobom trzecim.</p>

<h1>Jak się dostać do swoich danych osobistych</h1>
<p>Po załogowaniu można wejść do menu '<a href="myprofile.php">Twoj Profil</a>' i przejrzeć/zmienić przechowywane tam dane.</p>

<h1>Jak usunąć swoje dane</h1>
<p>Użytkownik, który założył lub znalazł skrzynkę nie może być usunięty.
    Jest możliwa tylko deaktywacja konta.
    Będą wtedy usunięte wszystkie dane e-mail z profilu ale nazwa użytkownika zostanie zachowana. Jeśli chcesz deaktywować swoje konto skontaktuj się z nami za pomocą swojego konta e-mail, a deaktywujemy Twoje konto.

<p>W przypadkach niejasnych prosimy o kontakt ocpl @ opencaching.pl Dziękujemy.</p>
